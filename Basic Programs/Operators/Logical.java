public class Logical {
    public static void main (String args [])

    {
        boolean a = true;
        boolean b = false;

        boolean c = a && b;
        boolean d = a || b;

        boolean e = !a;

        System.out.println("a && b : " +c);
        System.out.println("a || b : " +d);
        System.out.println("!a : " +e);
    }
}
