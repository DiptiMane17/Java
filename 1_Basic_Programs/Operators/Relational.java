public class Relational {
    public static void main(String args[])
    {
        int a = 5;
        int b = 4;

        System.out.println("First Number : " +a);
        System.out.println("Second Number : "+b);

        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a<=b);
        System.out.println(a>=b);
    }
    
}
